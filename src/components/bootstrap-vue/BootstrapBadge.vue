Variant default
```jsx
  <b-badge>label</b-badge>
```
Variant "secondary"
```jsx
  <b-badge
    variant="secondary"
  >
  label
  </b-badge>
```
Variant "outline-light"
```jsx
  <b-badge
    variant="outline-light"
  >
  label
  </b-badge>
```
Variant "pill"
```jsx
  <b-badge
    variant="pill"
  >
  label
  </b-badge>
```
